function initChannels () {
    let channels = getChannels();
    let config = $storage.open('config');
    let spectrum = $storage.open('spectrum');
    let configData = {
        'interval': {
            'width': 86400
        },
        'channelNumber': 0,
        'channels': {}
    };

    for (let index = 0; index < channels.length; index++) {
        let channel = channels[index];
        configData.channelNumber++;
        configData.channels[index] = {
            'color': channel.color,
            'mw': channel.mw
        };

        for (let wave in channel.waves) {
            $storage.append(spectrum, {
                'channel': index,
                'wave': wave,
                'value': channel.waves[wave]
            });
        }
    }

    $storage.append(config, configData);
    $storage.close(config);
    $storage.close(spectrum);
}
